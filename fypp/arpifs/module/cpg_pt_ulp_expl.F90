    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YIRAD%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YIRAD%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YIRAD%MPPT) = YDVARS%IRAD%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YLRAD%LT1 .AND. YGFL%YLRAD%LPT) THEN
    IF (YGFL%YLRAD%LADV) THEN
      IPT=YGFL%YLRAD%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YLRAD%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YLRAD%MPPT) = YDCPG_SL1%LRAD%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YLRAD%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YLRAD%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YLRAD%MPPT) = YDVARS%LRAD%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YS%LT1 .AND. YGFL%YS%LPT) THEN
    IF (YGFL%YS%LADV) THEN
      IPT=YGFL%YS%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YS%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YS%MPPT) = YDCPG_SL1%S%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YS%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YS%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YS%MPPT) = YDVARS%S%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YR%LT1 .AND. YGFL%YR%LPT) THEN
    IF (YGFL%YR%LADV) THEN
      IPT=YGFL%YR%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YR%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YR%MPPT) = YDCPG_SL1%R%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YR%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YR%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YR%MPPT) = YDVARS%R%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YG%LT1 .AND. YGFL%YG%LPT) THEN
    IF (YGFL%YG%LADV) THEN
      IPT=YGFL%YG%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YG%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YG%MPPT) = YDCPG_SL1%G%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YG%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YG%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YG%MPPT) = YDVARS%G%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YH%LT1 .AND. YGFL%YH%LPT) THEN
    IF (YGFL%YH%LADV) THEN
      IPT=YGFL%YH%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YH%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YH%MPPT) = YDCPG_SL1%H%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YH%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YH%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YH%MPPT) = YDVARS%H%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YTKE%LT1 .AND. YGFL%YTKE%LPT) THEN
    IF (YGFL%YTKE%LADV) THEN
      IPT=YGFL%YTKE%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YTKE%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YTKE%MPPT) = YDCPG_SL1%TKE%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YTKE%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YTKE%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YTKE%MPPT) = YDVARS%TKE%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YTTE%LT1 .AND. YGFL%YTTE%LPT) THEN
    IF (YGFL%YTTE%LADV) THEN
      IPT=YGFL%YTTE%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YTTE%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YTTE%MPPT) = YDCPG_SL1%TTE%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YTTE%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YTTE%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YTTE%MPPT) = YDVARS%TTE%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YEFB1%LT1 .AND. YGFL%YEFB1%LPT) THEN
    IF (YGFL%YEFB1%LADV) THEN
      IPT=YGFL%YEFB1%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB1%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB1%MPPT) = YDCPG_SL1%EFB1%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB1%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YEFB1%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB1%MPPT) = YDVARS%EFB1%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YEFB2%LT1 .AND. YGFL%YEFB2%LPT) THEN
    IF (YGFL%YEFB2%LADV) THEN
      IPT=YGFL%YEFB2%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB2%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB2%MPPT) = YDCPG_SL1%EFB2%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB2%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YEFB2%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB2%MPPT) = YDVARS%EFB2%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YEFB3%LT1 .AND. YGFL%YEFB3%LPT) THEN
    IF (YGFL%YEFB3%LADV) THEN
      IPT=YGFL%YEFB3%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB3%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB3%MPPT) = YDCPG_SL1%EFB3%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB3%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YEFB3%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YEFB3%MPPT) = YDVARS%EFB3%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YA%LT1 .AND. YGFL%YA%LPT) THEN
    IF (YGFL%YA%LADV) THEN
      IPT=YGFL%YA%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YA%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YA%MPPT) = YDCPG_SL1%A%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YA%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YA%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YA%MPPT) = YDVARS%A%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YO3%LT1 .AND. YGFL%YO3%LPT) THEN
    IF (YGFL%YO3%LADV) THEN
      IPT=YGFL%YO3%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YO3%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YO3%MPPT) = YDCPG_SL1%O3%P_X(KSTART:KPROF,1:NFLEVG)
    ELSE
