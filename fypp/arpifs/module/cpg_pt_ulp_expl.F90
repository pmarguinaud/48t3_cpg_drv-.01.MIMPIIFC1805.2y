    ELSE
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YSCONV%MPPT)=PGFLT1(KSTART:KPROF,1:NFLEVG,YGFL%YSCONV%MP1)
!     PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YSCONV%MPPT) = YDVARS%SCONV%T1(KSTART:KPROF,1:NFLEVG)
    ENDIF
  ENDIF

  IF (YGFL%YIRAD%LT1 .AND. YGFL%YIRAD%LPT) THEN
    IF (YGFL%YIRAD%LADV) THEN
      IPT=YGFL%YIRAD%MP_SLX
      PGFLPT(KSTART:KPROF,1:NFLEVG,YGFL%YIRAD%MPPT)=ZTENDGFL(KSTART:KPROF,IPT+1:IPT+NFLEVG)
